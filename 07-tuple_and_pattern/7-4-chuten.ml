(* Purpose: x座標とy座標の組で表された平面座標を二つ受け取ったらその中点の座標を返す *)
(* chuten : (float * float) -> (float * float) -> (float * float) *)
let chuten coords1 coords2 = match coords1 with (x1, y1) 
                          -> match coords2 with (x2, y2) 
                          -> ((x1 +. x2) /. 2.0, (y1 +. y2) /. 2.0)

